    private void cargarCombo(JComboBox c) {
        DefaultComboBoxModel<String> dispo = new DefaultComboBoxModel();
        c.setModel(dispo);
        for (Warehouse almacen: almacenes) {
            dispo.addElement(almacen.name);
        }
    }
